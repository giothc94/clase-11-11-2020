/**
 * modulos
 * palabras clave: module, exports
 */

module.exports.suma = function(parametro1, parametro2){
  return parametro1 + parametro2
}
module.exports.resta = function(parametro1, parametro2){
  return parametro1 - parametro2
}
module.exports.multiplicacion = (param1, param2) => param1 * param2
